export function tsStr(timestamp) {
  return new Date(timestamp).toISOString().slice(11, 23)
}

export function escHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
}
